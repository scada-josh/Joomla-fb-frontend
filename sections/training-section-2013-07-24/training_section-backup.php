    function updateTopicDescription(type_panel, param){
        $.ajax({
            url: '../utility/REST/getCategory_Description.php',
            data:{category_id: param},
            dataType:  'json',
            success: function(data){
                //console.log(data);
                $('#'+type_panel+'-category_header').html(data.category_header);
                $('#'+type_panel+'-category_description').html(data.category_desc);
            }
        });
    }

    function updateVideoList(containerVideoList, paramCategoryID){

        $('#'+containerVideoList).css('overflow','auto');

        $.ajax({
            url: '../utility/REST/getVideo_List.php',
            data:{category_id: paramCategoryID},
            dataType:  'json',
            success: function(jsonData){

                $('#'+containerVideoList).empty();

                $.each(jsonData.rows, function(idx, data) {
                    var headerTag = "<li class='nav-header'>"+data.header_name+"</li>";
                    $('#'+containerVideoList).append(headerTag);

                    $.each(data.videoList, function(idx, data_list) {
                            var videoTag = "<li class='media' id='"+data_list.sub_menu_id+"'>";
                            videoTag += "<a href='#''>";
                            videoTag += "<span class='displayNumber'>"+data_list.id+"."+"&nbsp;</span><span class='displayIconPlay' style='display:none'><i class='icon-play'></i></span>";
                            videoTag += "<img data-src='holder.js/140x140' class='img-polaroid' alt='' style='width: 40px; height: 40px;'' src='img/video_icon_default.png'>"
                            videoTag += "&nbsp;"+data_list.name;
                            videoTag += "<blockquote>";
                            videoTag += "<small>";
                            videoTag += data_list.dateUpdate+"<em> (&nbsp;<span class='displayViewCount'>"+data_list.viewCount+"</span> views&nbsp;)</em> &nbsp; <i class='icon-list-alt'></i> &nbsp;";
                            if(data_list.showStatus){
                                videoTag += "<img src='./sections/training-section/images/"+data_list.showStatus+".gif' alt=''>";
                            }
                            videoTag += "</small>";
                            videoTag += "</blockquote>";
                            videoTag += "</a>";
                            videoTag += "</li>";
                        $('#'+containerVideoList).append(videoTag);
                    });
                });

            }
        });

    }


    function showVideo(type_panel,paramMenu_id){
        $.ajax({
            url: '../utility/REST/getVideo.php',
            data:{menu_id: paramMenu_id},
            dataType:  'json',
            success: function(data){
                //console.log(data);



                if (data.member_type) {
                    var myYT = '<object  width="100%" height="600">';
                        myYT = myYT + '<param name="movie" value="https://www.youtube.com/v/'+data.youtube_code+'?version=3&vq=hd720"></param>';
                        myYT = myYT + '<param name="allowFullScreen" value="true"></param>';
                        myYT = myYT + '<param name="allowScriptAccess" value="always"></param>';
                        myYT = myYT + '<param name="setPlaybackQuality" value="hd720"></param>';
                        myYT = myYT + '<embed src="https://www.youtube.com/v/'+data.youtube_code+'?version=3&vq=hd720"';
                        myYT = myYT + 'type="application/x-shockwave-flash" ';
                        myYT = myYT + 'allowfullscreen="true" ';
                        myYT = myYT + 'allowScriptAccess="always" ';
                        myYT = myYT + 'width="100%" height="600">';
                        myYT = myYT + '</embed>';
                        myYT = myYT + '</object>';

                        if (data.youtube_code) {
                            $('#'+type_panel+'-showVideo-Area').html(myYT);
                        }else{
                            $('#'+type_panel+'-showVideo-Area').html('<img src="img/video_player_icon_small.jpg" alt="">');
                        }
                        
                        $('#'+type_panel+'-showVideoTagName-Area').html(data.sub_menu_name);
                        
                        var updateViewCount = $("#"+data.sub_menu_id);
                        updateViewCount.find('.displayViewCount').html(parseInt(updateViewCount.find('.displayViewCount').html())+1);

                }else{
                    alert('กรุณา Login ก่อนเข้าใช้งานระบบ');
                    window.location = 'index.php';
                }
            }
        });
